public enum ProductStateEnum {

    UP(0,"上架"),

    OUT(1,"下架"),

    ;

    Integer code;

    String desc;

    ProductStateEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
